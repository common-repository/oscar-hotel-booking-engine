<h1>Redforts Hotel Software motor de reservas</h1>

<p>Este plugin te permite mostrar su motor de reservas de Redforts Hotel Software
en su web de Wordpress.</p>

<p>Para mostrar el widget del motor de reservas sólo tiene que editar la
página y añadir el shortcode <code>[ohbe_search]</code>. Puede añadir el
widget en varias páginas y en varios sitios. Cada widget se puede ajustar con
parámetros del shortcode; vea la
<a href="https://oscar.redforts.com/es/setup,be,integration,wordpress"
target=_blank>página de integración</a> en su cuenta de Redforts Hotel Software.</p>

<p>Para más información sobre shortcodes visite <a
href="https://codex.wordpress.org/Shortcode_API"
target=_blank>wordpress.org</a>.</p>
