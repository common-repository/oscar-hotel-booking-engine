<h1>Redforts Hotel Software boekingsmodule</h1>

<p>Met deze plugin kunt u uw eigen boekingsmodule tonen op uw Wordpress
website.</p>

<p>Om de widget van de boekingsmodule te installeren, hoeft u alleen de
shortcode <code>[ohbe_search]</code> aan een pagina toe te voegen. U kunt de
module op meerdere pagina's en plekken plaatsen. De widget kan worden
aangepast door parameters aan de shortcode toe te voegen, zie de <a
href="https://oscar.redforts.com/nl/setup,be,integration,wordpress"
target=_blank>integratie pagina</a> in uw Redforts Hotel Software account.</p>

<p>Voor meer informatie over het gebruik van Wordpress shortcodes, zie <a
href=https://codex.wordpress.org/Shortcode_API
target=_blank>wordpress.org</a>.</p>
