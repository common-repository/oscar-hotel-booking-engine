<h1>Redforts Hotel Software booking engine</h1>

<p>This plugin allows you to show your Redforts Hotel Software booking engine on
your Wordpress website.</p>

<p>To show the booking engine widget you only have to edit a page and add
the shortcode <code>[ohbe_search]</code>. You can add the widget on various
pages and places. Each widget can be be tuned by adding parameters to the
shortcode; see the <a
href="https://oscar.redforts.com/en/setup,be,integration,wordpress"
target=_blank>integration page</a> in your Redforts Hotel Software account.</p>

<p>To learn more about Wordpress shortcodes, see <a
href=https://codex.wordpress.org/Shortcode_API
target=_blank>wordpress.org</a>.</p>
